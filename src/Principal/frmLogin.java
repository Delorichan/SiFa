    public void sNumeros(JTextField a){
        a.addKeyListener(new KeyAdapter() {
            public void keyTyped(KeyEvent e){
                char c = e.getKeyChar();
                if((c<'0'||c>'9') &&(c!=KeyEvent.VK_BACK_SPACE)){
                    getToolkit().beep();
                    e.consume();
                }
            }
        });
    }
    
    
