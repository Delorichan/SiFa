    public void logueo(int user,String pass){
        try {
            st=cn.prepareCall("{call logueo(?,?)}");
            st.setInt(1, user);
            st.setString(2, pass);
            r=st.executeQuery();
            if(r.next()){
                switch(r.getString(1)){
                    case "Administrador" :
                        frmAdmin a = new frmAdmin();
                        a.setVisible(true);
                        this.dispose();
                        break;
                    case "Usuario" :
                        frmUser b = new frmUser();
                        b.setVisible(true);
                        this.dispose();
                        break;
                }
            }else{
                getToolkit().beep();
                JOptionPane.showMessageDialog(rootPane, "Error en el Usuario o Contraseña","Error de Usuario",JOptionPane.ERROR_MESSAGE);
                txtContraseña.setText("");
                txtTrabajador.setText("");
                txtTrabajador.requestFocusInWindow();
            }
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(),"Error SQL",JOptionPane.ERROR_MESSAGE);
        }
    }
    
