    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        if(txtTrabajador.getText().equals("")){
            getToolkit().beep();
            JOptionPane.showMessageDialog(rootPane, "Ingrese el Numero de Trabajador","Error de Campos Vacios",JOptionPane.WARNING_MESSAGE);
            txtTrabajador.requestFocusInWindow();
            return;
        }
        if(txtContraseña.getText().equals("")){
            getToolkit().beep();
            JOptionPane.showMessageDialog(rootPane, "Ingrese la Contraseña","Error de Campos Vacios",JOptionPane.WARNING_MESSAGE);
            txtContraseña.requestFocusInWindow();
            return;
        }
        logueo(Integer.parseInt(txtTrabajador.getText()), txtContraseña.getText());
    }//GEN-LAST:event_btnAceptarActionPerformed

